#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	t_index			idx;
	unsigned char	*data;

	idx = 0;
	data = (unsigned char *)b;
	while (idx < len)
	{
		data[idx] = (unsigned char)c;
		idx++;
	}
	return (b);
}
